public class SumOfNum
{
	public static void main(String[] args)
		{
					//Goal is to find the sum of two numbers
					//Variable declarations
		int  numOne;		//Variable one
		double numTwo;		//Variable two
		double sum;		//Variable Three
					//Values assigned to variables
		numOne=1250;		//Value assigned to variable one
		numTwo=75.8;		//Value assigned to variable two
					//Sum calculation
		sum=(numOne+numTwo); 	//Assigns the sum of variables one and two to third variable
					//Displaying the result
		System.out.println("Here is the sum of the variables: "+ sum);//Prints sum

		}			//End of main
}					//End of class
